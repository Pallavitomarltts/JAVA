
public class Teamans2 {
		String name;
		long noofmatches;
		
		public Teamans2() {
			super();
		}
		public Teamans2(String name, long noofmatches) {
			super();
			this.name = name;
			this.noofmatches = noofmatches;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		public long getNoofmatches() {
			return noofmatches;
		}
		public void setNoofmatches(long noofmatches) {
			this.noofmatches = noofmatches;
		}
	}
